package org.fog.pfe.bss;

public class Mesure {

	double conE = 0;
	long load = 0;
	long bw = 0;
	double time = 0;
	
	
	public double getConE() {
		return conE;
	}
	public void setConE(double conE) {
		this.conE = conE;
	}
	public long getLoad() {
		return load;
	}
	public void setLoad(long load) {
		this.load = load;
	}
	public long getBw() {
		return bw;
	}
	public void setBw(long bw) {
		this.bw = bw;
	}
	public double getTime() {
		return time;
	}
	public void setTime(double time) {
		this.time = time;
	}
	
	
}
